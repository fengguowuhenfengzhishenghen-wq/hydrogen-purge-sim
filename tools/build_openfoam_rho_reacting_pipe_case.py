def build_case(
    case_id: str,
    window: str,
    out_root: Path,
    length_m: float,
    nx: int,
    ny: int,
    nz: int,
    end_time: float,
    dt: float,
    p_pa: float,
    t_k: float,
) -> Path:
    df_full = load_local_profile(case_id, window)
    df = select_window(df_full, length_m)
    source_x = df["x_case_m"].to_numpy(dtype=float)
    length_actual = float(source_x.max() - source_x.min())
    case_dir = out_root / f"{case_id}_rhoReactingPipe3D"
    if case_dir.exists():
        shutil.rmtree(case_dir)
    for sub in ("0", "constant", "system"):
        (case_dir / sub).mkdir(parents=True, exist_ok=True)

    cell_x = cell_x_centres(length_actual, nx, ny, nz)
    h2_mole = np.interp(cell_x, source_x, df["x_H2_molfrac"].to_numpy(dtype=float))
    n2_mole = np.interp(cell_x, source_x, df["x_N2_molfrac"].to_numpy(dtype=float))
    air_mole = np.interp(cell_x, source_x, df["x_Air_molfrac"].to_numpy(dtype=float))
    y_h2, y_o2, y_n2 = mole_to_mass(h2_mole, n2_mole, air_mole)

    write_block_mesh(case_dir / "system" / "blockMeshDict", length_actual, 1.2, nx, ny, nz)
    write_species(case_dir / "0" / "H2", "H2", y_h2)
    write_species(case_dir / "0" / "O2", "O2", y_o2)
    write_species(case_dir / "0" / "N2", "N2", y_n2)
    write_uniform_scalar(case_dir / "0" / "Ydefault", "Ydefault", "[0 0 0 0 0 0 0]", 0.0)
    write_uniform_scalar(case_dir / "0" / "T", "T", "[0 0 0 1 0 0 0]", t_k, wall_type="zeroGradient")
    write_uniform_scalar(case_dir / "0" / "nut", "nut", "[0 2 -1 0 0 0 0]", 0.0, wall_type="fixedValue")
    write_uniform_scalar(case_dir / "0" / "alphat", "alphat", "[1 -1 -1 0 0 0 0]", 0.0, wall_type="fixedValue")
    write_uniform_scalar(case_dir / "0" / "k", "k", "[0 2 -2 0 0 0 0]", 1.0e-6, wall_type="zeroGradient")
    write_uniform_scalar(case_dir / "0" / "epsilon", "epsilon", "[0 2 -3 0 0 0 0]", 1.0e-6, wall_type="zeroGradient")
    write_U(case_dir / "0" / "U")
    write_p(case_dir / "0" / "p", p_pa)
    write_g(case_dir / "constant" / "g")
    write_turbulence(case_dir / "constant" / "turbulenceProperties")
    write_thermo_files(case_dir)
    write_fv_files(case_dir)
    write_control(case_dir / "system" / "controlDict", end_time, dt)
    write_run_scripts(case_dir)
    write_readme(case_dir, case_id, window, length_actual, nx, ny, nz)

    init = pd.DataFrame(
        {
            "cell_x_m": cell_x,
            "Y_H2": y_h2,
            "Y_O2": y_o2,
            "Y_N2": y_n2,
            "sumY": y_h2 + y_o2 + y_n2,
        }
    )
    init.to_csv(case_dir / "initial_mass_fraction_cells.csv", index=False, encoding="utf-8-sig")
    return case_dir


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--case-id", default="DN1200_u7_p010_stop60")
    parser.add_argument("--window", default="full_isolation_zone_1200m")
    parser.add_argument("--out-root", type=Path, default=ROOT / "openfoam_cases")
    parser.add_argument("--length-m", type=float, default=48.0)
    parser.add_argument("--nx", type=int, default=48)
    parser.add_argument("--ny", type=int, default=12)
    parser.add_argument("--nz", type=int, default=12)
    parser.add_argument("--end-time", type=float, default=30.0)
    parser.add_argument("--dt", type=float, default=0.05)
    parser.add_argument("--p-pa", type=float, default=100000.0)
    parser.add_argument("--t-k", type=float, default=293.15)
    args = parser.parse_args()
    out = build_case(
        args.case_id,
        args.window,
        args.out_root,
        args.length_m,
        args.nx,
        args.ny,
        args.nz,
        args.end_time,
        args.dt,
        args.p_pa,
        args.t_k,
    )
    print(out)


if __name__ == "__main__":
    main()
