"""Build a runnable 3D circular-pipe OpenFOAM multi-species case.

The generated case is a local shutdown review case, not the 12 km full-pipe
solver.  It uses OpenFOAM's compressible reacting-flow field layout with
chemistry and combustion disabled:

* 3D circular pipe mesh from blockMesh.
* H2/O2/N2 mass-fraction fields mapped from the 1D H2/N2/Air mole fractions.
* Closed inlet/outlet and no-slip pipe wall for shutdown stratification review.
* Gravity enabled.

Air from the 1D model is split into O2 and N2 before writing OpenFOAM fields.
"""

from __future__ import annotations

